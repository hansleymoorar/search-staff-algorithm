    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // ADD STAFF
        
        String nic = JOptionPane.showInputDialog("Enter Staff NIC: ");
        txtnic.setText(nic);
        String fname = JOptionPane.showInputDialog("Enter Staff First Name: ");
        txtfname.setText(fname);
        String lname = JOptionPane.showInputDialog("Enter Staff Last Name: ");
        txtlname.setText(lname);
        String ages = JOptionPane.showInputDialog("Enter Staff Age: ");
        txtage.setText(ages);
         try {
            if (txtnic.getText().equals("") || txtfname.getText().equals("") || txtlname.getText().equals("") || txtage.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Field(s) cannot be empty.", "Warning", JOptionPane.WARNING_MESSAGE);
                clearall();
            }else if(nic.length() !=14){
                JOptionPane.showMessageDialog(null, "Wrong NIC", "Warning", JOptionPane.WARNING_MESSAGE);
                clearall();
            }else if(fname.length() > 14 || lname.length()  > 14){
                JOptionPane.showMessageDialog(null, "Name too lengthy", "Warning", JOptionPane.WARNING_MESSAGE);
                clearall();
            }else {
                DefaultTableModel model = (DefaultTableModel)table_staff.getModel();
                
        Vector myRow= new Vector <Object>(4);
        myRow.add(txtnic.getText().trim());
        myRow.add(txtfname.getText().trim());
        myRow.add(txtlname.getText().trim());
        myRow.add(txtage.getText().trim());
        model.addRow(myRow);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Unsuccessfull Attempt!"+ex);
        }
        
    }//GEN-LAST:event_btnaddActionPerformed

    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // UPDATE
        
        try {
            String sql ="UPDATE STAFF SET FIRSTNAME=?, LASTNAME=?, AGE=? WHERE NIC=?";
            ps = con.prepareStatement(sql);
            ps.setString(1, txtfname.getText());
            ps.setString(2, txtlname.getText());
            ps.setString(3, txtage.getText());
            ps.setString(4, txtnic.getText());
            ps.executeUpdate(); 
            view();
            clearall();
            JOptionPane.showMessageDialog(null, "Record updated successfully");
            }catch(HeadlessException | SQLException sqe){
                JOptionPane.showMessageDialog(null, "Unsuccessfull Attempt!"+sqe); 
        }
    }//GEN-LAST:event_btnupdateActionPerformed

