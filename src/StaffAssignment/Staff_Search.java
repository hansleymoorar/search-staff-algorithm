    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // SEARCH
        view();
        DefaultTableModel model = (DefaultTableModel) table_staff.getModel();
        int rowCount = model.getRowCount();
        String StaffArr[] = new String[rowCount];
        for(int i = 0; i<rowCount; i++){
            StaffArr[i] = model.getValueAt(i, 1).toString();
        }
        //Use of linear seach 
        String Search = txtsearch.getText();
        int count = linearSearch(StaffArr, Search);
        if(count == -1 ){
            

            JOptionPane.showMessageDialog(null, "Result not found");
            
        }else{
            table_staff.clearSelection();
            table_staff.addRowSelectionInterval(count, count);
            txtnic.setText(model.getValueAt(count, 0).toString());
            txtfname.setText(model.getValueAt(count, 1).toString());
            txtlname.setText(model.getValueAt(count, 2).toString());
            txtage.setText(model.getValueAt(count, 3).toString());
            JOptionPane.showMessageDialog(null, "Result found");
        }
    }//GEN-LAST:event_btnsearchActionPerformed

